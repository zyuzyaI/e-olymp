# 950 Четыре площади
import math
n=input().split()
a,b,x,y=int(n[0]),int(n[1]),int(n[2]),int(n[3])
A=[0,0]
P=[0,b]
Q=[a,b]
M=[a,0]
B=[0,y]
O=[a,y]
C=[x,y]
K=[x,b]
D=[x,0]
s1=0.5*(math.fabs((A[0]-P[0])*(A[1]+P[1])+(P[0]-Q[0])*(P[1]+Q[1])+(Q[0]-M[0])*(Q[1]+M[1])+(M[0]-A[0])*(M[1]+A[1])))
s2=0.5*(math.fabs((P[0]-B[0])*(P[1]+B[1])+(B[0]-O[0])*(B[1]+O[1])+(O[0]-Q[0])*(O[1]+Q[1])+(Q[0]-P[0])*(Q[1]+P[1])))
s4=0.5*(math.fabs((M[0]-Q[0])*(M[1]+Q[1])+(Q[0]-K[0])*(Q[1]+K[1])+(K[0]-D[0])*(K[1]+D[1])+(D[0]-M[0])*(D[1]+M[1])))
s3=0.5*(math.fabs((Q[0]-O[0])*(Q[1]+O[1])+(O[0]-C[0])*(O[1]+C[1])+(C[0]-K[0])*(C[1]+K[1])+(K[0]-Q[0])*(K[1]+Q[1])))
print('%.3f %.3f %.3f %.3f' %(s1, s2, s3, s4))
